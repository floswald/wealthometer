		$("#mytax").html(nf(mytax, 2, ",", ".") + " €");
		$("#mytaxbalance").html(nf(percapita - mytax, 2, ",", ".") + " €");

		min = Math.floor(myModel[98]/1000)*1000;
		max = Math.ceil(myModel[0]/1000)*1000;
		steps = (max - min ) / 1000;

